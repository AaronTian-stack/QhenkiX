	struct ImageBarrier // VK_KHR_synchronization2
	{
		void* resource = nullptr;
		bool discard = false;
        // Sync Flags
		SyncStage src_stage;
		SyncStage dst_stage;
        // Access Flags
		AccessFlags src_access;
        AccessFlags dst_access;
        // Layout
		Layout src_layout;
		Layout dst_layout;
		// Subresource range
		ImageSubresourceRange subresource_range;
	};

