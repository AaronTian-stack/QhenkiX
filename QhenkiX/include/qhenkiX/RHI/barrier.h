    // https://registry.khronos.org/vulkan/specs/latest/man/html/VkAccessFlagBits2.html
	enum AccessFlags : uint32_t
	{
		ACCESS_COMMON = BIT(0),
		ACCESS_VERTEX_BUFFER = BIT(1), // VK_ACCESS_2_VERTEX_ATTRIBUTE_READ_BIT 
		ACCESS_UNIFORM_BUFFER = BIT(2), // CONSTANT_BUFFER, VK_ACCESS_2_UNIFORM_READ_BIT 
		ACCESS_INDEX_BUFFER = BIT(3), // VK_ACCESS_2_INDEX_READ_BIT 
		ACCESS_RENDER_TARGET = BIT(4), // VK_ACCESS_2_COLOR_ATTACHMENT_WRITE_BIT 
		ACCESS_STORAGE_ACCESS = BIT(5), // UNORDERED_ACCESS, VK_ACCESS_2_SHADER_WRITE_BIT, VK_ACCESS_2_SHADER_READ_BIT
		ACCESS_DEPTH_STENCIL_WRITE = BIT(6), // VK_ACCESS_2_DEPTH_STENCIL_ATTACHMENT_WRITE_BIT 
		ACCESS_DEPTH_STENCIL_READ = BIT(7), // VK_ACCESS_2_DEPTH_STENCIL_ATTACHMENT_READ_BIT 
		ACCESS_SHADER_RESOURCE = BIT(8), // 
		ACCESS_STREAM_OUTPUT = BIT(9),
		ACCESS_INDIRECT_ARGUMENT = BIT(10), // VK_ACCESS_2_INDIRECT_COMMAND_READ_BIT 
		//ACCESS_PREDICATION = BIT(11),
		ACCESS_COPY_DEST = BIT(12), // VK_ACCESS_2_TRANSFER_READ_BIT 
		ACCESS_COPY_SOURCE = BIT(13), // VK_ACCESS_2_TRANSFER_WRITE_BIT_KHR 
		//ACCESS_RESOLVE_DEST = BIT(14),
		//ACCESS_RESOLVE_SOURCE = BIT(15),
		ACCESS_RAYTRACING_ACCELERATION_STRUCTURE_READ = BIT(16),
		ACCESS_RAYTRACING_ACCELERATION_STRUCTURE_WRITE = BIT(17),
		ACCESS_SHADING_RATE_SOURCE = BIT(18),
		ACCESS_VIDEO_DECODE_READ = BIT(19),
		ACCESS_VIDEO_DECODE_WRITE = BIT(20),
		ACCESS_VIDEO_PROCESS_READ = BIT(21),
		ACCESS_VIDEO_PROCESS_WRITE = BIT(22),
		ACCESS_VIDEO_ENCODE_READ = BIT(23),
		ACCESS_VIDEO_ENCODE_WRITE = BIT(24),
		NO_ACCESS = BIT(25) // VK_ACCESS_2_NONE
		// No VK_ACCESS_2_INPUT_ATTACHMENT_READ_BIT due to no input attachments
	};
